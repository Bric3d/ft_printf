#include "libft.h"

int	ft_putstr_int(char *s)
{
	int i;

	i = 0;
	if (s)
	{
		i = ft_strlen(s);
		write(1, s, i);
	}
	else
	{
		i = 6;
		write(1, "(null)", i);
	}
	return (i);
}